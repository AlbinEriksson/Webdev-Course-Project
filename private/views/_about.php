<h1>Webshop</h1>
<p>
    Webshop was a project carried out by five computer science students at Mälardalen University as
    an examination in the course DVA231 <em>"Development of Web Applications."</em> The project
    began on the 10th of October, 2020, and had its final presentation on the 2nd of November, 2020.
</p>
<p>
    This website was developed on an Apache Server with a PHP backend and MySQL database. Some of
    the main focus points were:
    <ul>
        <li>Responsive design</li>
        <li>Exercising AJAX</li>
        <li>Admin controls</li>
        <li>An API for use with AJAX</li>
        <li>A well-structured DOM without duplicated or redundant elements</li>
        <li>Support for a wide range of browsers</li>
        <li>No use of external libraries to improve load times</li>
        <li>Modular and generic CSS classes to minimize repetition of CSS rules</li>
    </ul>
</p>
<p>
    We would like to acknowledge the potential for further development on this website, including,
    but not limited to:
    <ul>
        <li>A system for checkouts and warehouse for keeping track of available products</li>
        <li>Form input validation</li>
        <li>A method to reset passwords</li>
        <li>User profile</li>
    </ul>
</p>
